  dplyr::mutate(c_label2 = as.character(ifelse(c_label == "", row_number(), c_label))) %>%
  dplyr::ungroup() %>%
  dplyr::mutate(c_label = c_label2)

# filter for continuous variables with only one sample for each sub population
